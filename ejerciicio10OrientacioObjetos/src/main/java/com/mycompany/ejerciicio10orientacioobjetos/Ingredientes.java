
package com.mycompany.ejerciicio10orientacioobjetos;

public class Ingredientes {
    private float queso;
    private float champiñones;
    private float bacon;
    private float aceitunas;
    private float atun;
    private float verduras;
    private float pollo;
    private float piña;
    private float huevo;
    private float cebolla;

    public Ingredientes() {
        this.queso = 1f;
        this.champiñones = 1f;
        this.bacon = 1f;
        this.aceitunas = 1f;
        this.atun = 1f;
        this.verduras = 1;
        this.pollo = 1.5f;
        this.piña = 1f;
        this.huevo = 1.5f;
        this.cebolla = 1f;
    }

    public float getQueso() {
        return queso;
    }

    public void setQueso(float queso) {
        this.queso = queso;
    }

    public float getChampiñones() {
        return champiñones;
    }

    public void setChampiñones(float champiñones) {
        this.champiñones = champiñones;
    }

    public float getBacon() {
        return bacon;
    }

    public void setBacon(float bacon) {
        this.bacon = bacon;
    }

    public float getAceitunas() {
        return aceitunas;
    }

    public void setAceitunas(float aceitunas) {
        this.aceitunas = aceitunas;
    }

    public float getAtun() {
        return atun;
    }

    public void setAtun(float atun) {
        this.atun = atun;
    }

    public float getVerduras() {
        return verduras;
    }

    public void setVerduras(float verduras) {
        this.verduras = verduras;
    }

    public float getPollo() {
        return pollo;
    }

    public void setPollo(float pollo) {
        this.pollo = pollo;
    }

    public float getPiña() {
        return piña;
    }

    public void setPiña(float piña) {
        this.piña = piña;
    }

    public float getHuevo() {
        return huevo;
    }

    public void setHuevo(float huevo) {
        this.huevo = huevo;
    }

    public float getCebolla() {
        return cebolla;
    }

    public void setCebolla(float cebolla) {
        this.cebolla = cebolla;
    }
    
    
    
}
